int	ft_atoi(char *str)
{
	int	num;
	int	sign;

	num = 0;
	sign = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		++str;
	while (*str == 43 || *str == 45)
	{
		if (*str == 45)
			sign++;
		++str;
	}
	while (*str >= 48 && *str <= 57)
	{
		num *= 10;
		num += *str - 48;
		++str;
	}
	if (!(sign % 2))
		return (num);
	else
		return (-num);
}
