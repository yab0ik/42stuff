int	ft_atoi(char *str)
{
	int	i;
	int	neg;
	int	nb;

	i = 0;
	neg = 0;
	nb = 0;
	while ((str[i] > 8 && str[i] < 14) || (str[i] == 32))
		i++;
	while ((str[i] != '\0') && (str[i] == '+' || str[i] == '-'))
	{
		if (str[i] == '-')
			neg++;
		i++;
	}
	while ((str[i] != '\0') && (str[i] >= 48 && str[i] <= 57))
	{
		nb = nb * 10 + str[i] - 48;
		i++;
	}
	if (neg % 2 != 0)
		return (nb * -1);
	return (nb);
}
